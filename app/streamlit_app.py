"""
Gemstone Financial — Semantic Layer Demo
=============================================
Streamlit visualization for the four-tier Cortex Analyst variance study.

Loads the most recent run from variance/results/*.json and renders:
  1. Header scorecard  — accuracy across all four tiers at a glance
  2. Summary bar chart — Status breakdown by tier (stacked bar)
  3. Comparison table  — One row per question, expandable per tier
  4. Detail expanders  — Full SQL, result rows, ground truth, failure narrative

Four tiers (any subset may be present in a given results file):
  bronze     — raw Bronze tables (fragmented, no cross-source joins)
  silver     — SILVER.POSITIONS_INTEGRATED (naive, A7–A11 embedded)
  gold_naive — GOLD_NAIVE.POSITIONS_NAIVE (assumption-based, possibly worse)
  gold       — GOLD DW + semantic model (governed, correct answers)

Requires a results file generated by:
  python variance/runner.py

Run with:
  streamlit run app/streamlit_app.py
"""

import json
import subprocess
import sys
from pathlib import Path

import pandas as pd
import plotly.graph_objects as go
import streamlit as st

sys.path.insert(0, str(Path(__file__).parent.parent))
from variance.comparator import score
from variance.ground_truth import compute_all
from variance.questions import QUESTIONS

_PROJECT_ROOT = Path(__file__).parent.parent
_RESULTS_DIR = _PROJECT_ROOT / "variance" / "results"

ALL_MODELS = ["bronze", "silver", "gold_naive", "gold"]

_MODEL_LABELS = {
    "bronze":     "Bronze (raw)",
    "silver":     "Silver (naive)",
    "gold_naive": "Naive Gold",
    "gold":       "Semantic Gold",
}

_MODEL_DESCRIPTION = {
    "bronze":     "Raw Bronze tables — fragmented schemas, no cross-source joins",
    "silver":     "SILVER.POSITIONS_INTEGRATED — naive union, A7–A11 embedded",
    "gold_naive": "GOLD_NAIVE.POSITIONS_NAIVE — assumption-based Gold, possibly worse",
    "gold":       "GOLD DW + semantic model — governed, correct answers",
}

_STATUS_EMOJI = {
    "CORRECT": "✅",
    "WRONG":   "❌",
    "NO_DATA": "⚪",
    "ERROR":   "⚠️",
}

_STATUS_COLOR = {
    "CORRECT": "#2ecc71",
    "WRONG":   "#e74c3c",
    "NO_DATA": "#f39c12",
    "ERROR":   "#95a5a6",
}

_STATUS_LABEL = {
    "CORRECT": "Correct",
    "WRONG":   "Wrong",
    "NO_DATA": "No data",
    "ERROR":   "Error",
}


@st.cache_data
def _cached_ground_truths() -> dict:
    return compute_all()


def _rescore(data: dict) -> dict:
    """Re-apply comparator scoring to raw rows in a results dict."""
    ground_truths = _cached_ground_truths()
    q_map = {q.id: q for q in QUESTIONS}

    for q_entry in data.get("questions", []):
        qid = q_entry["id"]
        q = q_map.get(qid)
        if q is None:
            continue
        gt = ground_truths[qid]
        for model in ALL_MODELS:
            mdata = q_entry.get(model)
            if mdata is None:
                continue
            if mdata.get("error"):
                mdata["status"] = "ERROR"
                mdata["variance"] = None
                mdata["value"] = None
            else:
                rows = mdata.get("rows") or []
                status, variance, value = score(rows, gt, q.result_type)
                mdata["status"] = status
                mdata["variance"] = variance
                mdata["value"] = value

    summary = data.setdefault("summary", {})
    questions = data.get("questions", [])
    total = len(questions)
    summary["total_questions"] = total
    for model in ALL_MODELS:
        statuses = [
            q.get(model, {}).get("status", "ERROR")
            for q in questions
            if q.get(model) is not None
        ]
        if statuses:
            correct = statuses.count("CORRECT")
            summary[f"{model}_correct"] = correct
            summary[f"{model}_accuracy_pct"] = round(correct / total * 100.0, 1)

    return data


def _badge(status: str | None) -> str:
    if not status:
        return "—"
    emoji = _STATUS_EMOJI.get(status, "?")
    label = _STATUS_LABEL.get(status, status)
    return f"{emoji} {label}"


def _fmt_value(value, result_type: str, ground_truth: float) -> str:
    if value is None:
        return "—"
    if result_type == "row_count":
        return f"{int(value):,} rows"
    if result_type == "percentage":
        return f"{value:.2f}%"
    if abs(ground_truth) >= 1_000:
        return f"${value:,.2f}"
    return f"{value:,.4f}"


def _fmt_gt(ground_truth: float, result_type: str) -> str:
    if result_type == "row_count":
        return f"{int(ground_truth):,} rows"
    if result_type == "percentage":
        return f"{ground_truth:.4f}%"
    return f"${ground_truth:,.2f}"


def _fmt_variance(variance, result_type: str, status: str) -> str:
    if variance is None or status in ("NO_DATA", "ERROR"):
        return "—"
    sign = "+" if variance > 0 else ""
    if result_type == "row_count":
        return f"{sign}{int(variance):,} rows"
    if result_type == "percentage":
        return f"{sign}{variance:.2f} pp"
    return f"{sign}{variance:.2f}%"


def _load_latest_results() -> dict | None:
    json_files = sorted(_RESULTS_DIR.glob("*.json"), reverse=True)
    if not json_files:
        return None
    return json.loads(json_files[0].read_text())


def _list_result_files() -> list[Path]:
    return sorted(_RESULTS_DIR.glob("*.json"), reverse=True)


