# ── Summary bar chart ─────────────────────────────────────────────────────────

def _build_summary_chart(questions: list[dict], models: list[str]) -> go.Figure:
    status_order = ["CORRECT", "WRONG", "NO_DATA", "ERROR"]
    counts: dict[str, dict[str, int]] = {m: {s: 0 for s in status_order} for m in models}

    for q in questions:
        for m in models:
            mdata = q.get(m) or {}
            s = mdata.get("status", "ERROR")
            counts[m][s] = counts[m].get(s, 0) + 1

    fig = go.Figure()
    x_labels = [_MODEL_LABELS.get(m, m) for m in models]
    for status in status_order:
        y_vals = [counts[m][status] for m in models]
        if sum(y_vals) == 0:
            continue
        fig.add_trace(go.Bar(
            name=_STATUS_LABEL[status],
            x=x_labels,
            y=y_vals,
            marker_color=_STATUS_COLOR[status],
            text=y_vals,
            textposition="inside",
            insidetextanchor="middle",
        ))

    fig.update_layout(
        barmode="stack",
        title="Result status by tier",
        yaxis_title="Questions",
        yaxis=dict(tickmode="linear", dtick=1),
        legend=dict(orientation="h", yanchor="bottom", y=1.02, xanchor="right", x=1),
        height=340,
        margin=dict(l=0, r=0, t=50, b=0),
        plot_bgcolor="rgba(0,0,0,0)",
        paper_bgcolor="rgba(0,0,0,0)",
    )
    return fig


if visible_models:
    st.subheader("Result summary")
    st.plotly_chart(_build_summary_chart(questions, visible_models), use_container_width=True)
    st.divider()

# ── Comparison table ──────────────────────────────────────────────────────────

st.subheader("Question-by-question comparison")
st.caption("Expand a row to see the generated SQL, results, and failure narrative.")

for q in questions:
    qid = q["id"]
    codes = ", ".join(q["ambiguity_codes"])
    question_short = q["question"][:70] + ("…" if len(q["question"]) > 70 else "")
    gt = q["ground_truth"]
    result_type = q["result_type"]

    # Build compact status line for expander header
    status_parts = []
    for m in visible_models:
        mdata = q.get(m) or {}
        status = mdata.get("status", "ERROR")
        status_parts.append(f"{_MODEL_LABELS[m]}: {_badge(status)}")
    status_line = "  ·  ".join(status_parts)

    expander_label = f"**{qid}** [{codes}]  {question_short}   —   {status_line}"

    with st.expander(expander_label):
        st.markdown(
            f"**Ground truth:** {_fmt_gt(gt, result_type)}  "
            f"·  *{q['ground_truth_description']}*"
        )
        st.divider()

        n_cols = len(visible_models)
        cols = st.columns(n_cols) if n_cols > 0 else []

        for idx, model in enumerate(visible_models):
            mdata = q.get(model) or {}
            status = mdata.get("status", "ERROR")
            value = mdata.get("value")
            variance = mdata.get("variance")
            sql = mdata.get("sql") or ""
            rows = mdata.get("rows") or []
            error = mdata.get("error")

            with cols[idx]:
                st.markdown(f"#### {_MODEL_LABELS[model]}")
                st.markdown(
                    f"**Status:** {_badge(status)}  "
                    f"**Value:** {_fmt_value(value, result_type, gt)}  "
                    f"**Δ:** {_fmt_variance(variance, result_type, status)}"
                )

                if error:
                    st.error(f"Error: {error}")

                if sql:
                    st.markdown("**Generated SQL:**")
                    st.code(sql, language="sql")

                if rows:
                    st.markdown(f"**Result** ({len(rows)} rows):")
                    st.dataframe(
                        pd.DataFrame(rows[:10]),
                        use_container_width=True,
                        hide_index=True,
                    )
                    if len(rows) > 10:
                        st.caption(f"… {len(rows) - 10} more rows not shown")
                elif not error:
                    st.info("No rows returned.")

        if q.get("failure_mode_silver"):
            st.divider()
            st.markdown("**Why ungoverned layers fail here:**")
            st.info(q["failure_mode_silver"])

st.divider()
st.caption(
    f"Run ID: {data.get('run_id', '—')}  ·  "
    f"Timestamp: {data.get('run_timestamp', '—')[:19].replace('T', ' ')} UTC  ·  "
    "Ground truth computed from `data/seed_v2/` (independent of Snowflake)"
)
