    pct = summary.get(f"{model}_accuracy_pct")
    with scorecard_cols[idx]:
        label = _MODEL_LABELS[model]
        delta_color = "normal" if model == "gold" else "inverse"
        st.metric(
            label,
            f"{correct}/{total} correct",
            delta=f"{pct:.0f}% accuracy",
            delta_color=delta_color,
        )
        st.caption(_MODEL_DESCRIPTION[model])

